             <b style="font-size:150%">Framework of Mentorship Programme</b><br><br>
              Under this programme, we invite our alumni across the globe and from different fields to interact with the students and help them build their network and gain a new perspective on the possibilities open to them, after graduation.

    <br><br>

    With the students facing crossroads at every other turn and unable to choose what's best for them due to a lack of insight into how things would actually materialize with each road that they choose to take, it becomes crucial that they get advice from people who have faced the very same crossroads in their student life and the big bad competitive world beyond it.

    <br><br>

    The Alumni with their vast repository of personal experiences can help improve insight among students into so many diverse aspects of life which include academics, extracurriculars, the professional life after college or the general way of life. This is where the <b> Student Alumni Mentorship Programme </b>comes into the picture. 

    <br><br>

    It facilitates a medium where the student connects with an Alumnus from a field he has a profound interest in. He gives his preference of field, year and place while registering online and he is accordingly allotted a mentor based on a predefined code. In the process of learning the ropes in that particular field he takes home so much more from the guide, coach, supporter and motivator, he finds in his mentor.
    <br> <br>
    <b style="font-size:150%">Connecting with our Alumni</b><br><br>
    <b>The Student Alumni Mentorship Programme</b> is also an active step to keep the alumni in touch with all the 
    happenings at IIT Kharagpur. 
    The Institute has undergone many changes from time to time- While some are quite evident like the construction and 
    commencement of classes at the new Nalanda Complex, others are a bit subtle like the formation of interesting 
    new societies, changes in the General Championship standings and other transitions that the Institute is undergoing.
     With an 
     all year round interaction with the students, the Students&#39; Alumni Mentorship Programme ensures that the Alumni be 
     in the know of the events as they occur and helps them relive their own KGP days.

